--FUNCTION TO DISPLAY TOTAL EMPLOYEE COUNT FOR THAT ORG
CREATE OR REPLACE FUNCTION SF_ORG_ACT_EMP(P_ORG VARCHAR2)
RETURN NUMBER
AS
  LN_TOTAL_COUNT NUMBER;
BEGIN
  SELECT SUM(MO.EMPLOYEES_COUNT)
  INTO LN_TOTAL_COUNT
  FROM MONTHLY_ORG_SAL_DTLS MO
  JOIN MST_ORG_DTLS MD
    ON MO.ORG_ID = MD.ORG_ID
  WHERE MD.ORG_NAME = P_ORG;
  
  RETURN LN_TOTAL_COUNT;

EXCEPTION
  WHEN OTHERS THEN
    RETURN 0;
END;

