    IF NOT X THEN
      RAISE_APPLICATION_ERROR(-20001, I.ORG_NAME || ' FILE NOT FOUND');
    END IF;
  
    LV_QUERY := 'ALTER TABLE EXT_MONTHLY_ORG_EMP_DTLS LOCATION (''' ||
                LV_EMP_FILE || ''')';
    EXECUTE IMMEDIATE LV_QUERY;
  
    LV_ORG_FILE := 'BANK_MINUS_ORG_' || TO_CHAR(SYSDATE, 'MM_YYYY') ||
                   '.CSV';
  
    FO := UTL_FILE.FOPEN('DATA_PUMP_BSM', LV_ORG_FILE, 'W');
  
    UTL_FILE.PUT_LINE(FO,
                      'EMP_ID,EMP_NAME,SALARY,ORG_ID,PHONE_NUM,MAIL_ID,DATE_OF_ACTIVE,EMP_STATUS');
  
    FOR J IN (SELECT *
                FROM ORG_EMP_DTLS OE
               WHERE OE.ORG_ID = I.ORG_ID
              MINUS
              SELECT *
                FROM EXT_MONTHLY_ORG_EMP_DTLS EM
               WHERE EM.ORG_ID = I.ORG_ID) LOOP
    
      UTL_FILE.PUT_LINE(FO,
                        J.EMP_ID || ',' || J.EMP_NAME || ',' || J.SALARY || ',' ||
                        J.ORG_ID || ',' || J.PHONE_NUM || ',' || J.MAIL_ID || ',' ||
                        J.DATE_OF_ACTIVE || ',' || J.EMP_STATUS);
    END LOOP;
    UTL_FILE.fclose(FO);
  
    LV_ORG_FILE := 'ORG_MINUS_BANK_' || TO_CHAR(SYSDATE, 'MM_YYYY') ||
                   '.CSV';
  
    FO := UTL_FILE.FOPEN('DATA_PUMP_BSM', LV_ORG_FILE, 'W');
  
    UTL_FILE.PUT_LINE(FO,
                      'EMP_ID,EMP_NAME,SALARY,ORG_ID,PHONE_NUM,MAIL_ID,DATE_OF_ACTIVE,EMP_STATUS');
    FOR K IN (SELECT *
                FROM EXT_MONTHLY_ORG_EMP_DTLS EM
               WHERE EM.ORG_ID = I.ORG_ID
              MINUS
              SELECT *
                FROM ORG_EMP_DTLS OE
               WHERE OE.ORG_ID = I.ORG_ID) LOOP
      UTL_FILE.PUT_LINE(FO,
                        K.EMP_ID || ',' || K.EMP_NAME || ',' || K.SALARY || ',' ||
                        K.ORG_ID || ',' || K.PHONE_NUM || ',' || K.MAIL_ID || ',' ||
                        K.DATE_OF_ACTIVE || ',' || K.EMP_STATUS);
    END LOOP;
  
    UTL_FILE.fclose(FO);
  
  END LOOP;
END SP_EMP_COMPARE;

