--DISPLAY TOTAL SALARY TO BE PAID TO ORG
CREATE OR REPLACE FUNCTION SF_SAL_TO_PAY_ORG(P_ORG VARCHAR2)
RETURN NUMBER
AS
  LN_TOTAL_SAL NUMBER;
BEGIN
  SELECT SUM(MO.TOTAL_SAL)
  INTO LN_TOTAL_SAL
  FROM MONTHLY_ORG_SAL_DTLS MO
  JOIN MST_ORG_DTLS MD
    ON MO.ORG_ID = MD.ORG_ID
  WHERE MD.ORG_NAME = P_ORG
    AND MO.MONTH = TO_NUMBER(TO_CHAR(SYSDATE,'MM'))
    AND MO.YEAR  = TO_NUMBER(TO_CHAR(SYSDATE,'YYYY'));

  RETURN LN_TOTAL_SAL;

EXCEPTION
  WHEN OTHERS THEN
    RETURN 0;
END;

