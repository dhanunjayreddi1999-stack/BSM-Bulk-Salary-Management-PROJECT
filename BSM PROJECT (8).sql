         MAIL_ID, DATE_OF_ACTIVE, EMP_STATUS, SYSDATE
  FROM ORG_EMP_DTLS
  WHERE EMP_STATUS = 'X';

  COMMIT;

END SP_BACKUP_X_DATA;

