CREATE OR REPLACE PROCEDURE SP_DATA_MIGRATION AS
  X BOOLEAN;
  Y INTEGER;
  Z BINARY_INTEGER;

  LV_ORG_FILE VARCHAR2(500);
  LV_QUERY    VARCHAR2(1000);
  LV_ORG_NAME VARCHAR2(30);
  LV_EMP_FILE VARCHAR2(500);

  CURSOR C1 IS
    SELECT EM.ORG_NAME FROM EXT_MST_ORG_DTLS EM WHERE EM.ORG_STATUS='A';
    CURSOR C2 IS
    SELECT EM.ORG_NAME,EM.ORG_ID FROM EXT_MST_ORG_DTLS EM WHERE EM.ORG_STATUS='X';

BEGIN
  LV_ORG_FILE := 'NEW_ORG_DTLS_' || TO_CHAR(SYSDATE, 'DD_MM_YYYY') ||
                 '.CSV';
  UTL_FILE.fgetattr('DATA_PUMP_BSM', LV_ORG_FILE, X, Y, Z);

  IF X THEN
    LV_QUERY := 'ALTER TABLE EXT_MST_ORG_DTLS LOCATION ( ' || CHR(39) ||
                LV_ORG_FILE || CHR(39) || ' )';
    EXECUTE IMMEDIATE LV_QUERY;
  
    MERGE INTO MST_ORG_DTLS T
    USING EXT_MST_ORG_DTLS S
    ON (T.ORG_NAME = S.ORG_NAME)
    WHEN MATCHED THEN
      UPDATE
         SET T.ORG_CP_NAME         = S.ORG_CP_NAME,
             T.ORG_CP_MAIL_ID      = S.ORG_CP_MAIL_ID,
             T.ORG_NO_OF_EMPLOYEES = S.ORG_NO_OF_EMPLOYEES,
             T.ORG_SALARY_DATE     = S.ORG_SALARY_DATE,
             T.ORG_STATUS          = S.ORG_STATUS
    WHEN NOT MATCHED THEN
      INSERT
      VALUES
        (s.org_id,
         S.ORG_NAME,
         S.ORG_PHONE_NUM,
         S.ORG_MAIL_ID,
         S.ORG_ADDRESS,
         S.ORG_CP_NAME,
         S.ORG_CP_MAIL_ID,
         S.ORG_DEAL_DATE,
         S.ORG_NO_OF_EMPLOYEES,
         S.ORG_SALARY_DATE,
         S.ORG_STATUS);
  
  ELSE
    RAISE_APPLICATION_ERROR(-20333, 'ORGANIZATION FILE NOT FOUND');
  END IF;

  FOR I IN C1 LOOP
    LV_ORG_NAME := I.ORG_NAME;
  
    LV_EMP_FILE := LV_ORG_NAME || '_EMP_DTLS_' ||
                   TO_CHAR(SYSDATE, 'DD_MM_YYYY') || '.CSV';
    UTL_FILE.fgetattr('DATA_PUMP_BSM', LV_EMP_FILE, X, Y, Z);
  
    IF X THEN
      LV_QUERY := 'ALTER TABLE EXT_ORG_EMP_DTLS LOCATION ( ' || CHR(39) ||
                  LV_EMP_FILE || CHR(39) || ' )';
      EXECUTE IMMEDIATE LV_QUERY;
    
      MERGE INTO ORG_EMP_DTLS T
      USING EXT_ORG_EMP_DTLS S
      ON (T.EMP_ID = S.EMP_ID AND T.ORG_ID = S.ORG_ID)
      WHEN MATCHED THEN
        UPDATE SET T.SALARY = S.SALARY, T.EMP_STATUS = S.EMP_STATUS
      WHEN NOT MATCHED THEN
        INSERT
        VALUES
          (S.EMP_ID,
           S.EMP_NAME,
           S.SALARY,
           S.ORG_ID,
           S.PHONE_NUM,
           S.MAIL_ID,
           S.DATE_OF_ACTIVE,
           S.EMP_STATUS);
    
    ELSE
      RAISE_APPLICATION_ERROR(-20333, LV_ORG_NAME || ' EMP FILE NOT FOUND');
    END IF;
  END LOOP;
  
  FOR I IN C2 LOOP
    UPDATE ORG_EMP_DTLS OE SET OE.EMP_STATUS='X' WHERE OE.ORG_ID=I.ORG_ID;
    END LOOP;
EXCEPTION 
  WHEN OTHERS THEN
    sp_error_log('SP_DATA_MIGRATION',SQLERRM,DBMS_UTILITY.format_error_backtrace);
        RAISE_APPLICATION_ERROR(-20300, SQLERRM);

END SP_DATA_MIGRATION;


