CREATE OR REPLACE PROCEDURE SP_TOMORROW_SAL_FILES
AS
  FO UTL_FILE.FILE_TYPE;
  LV_ORG_FILE VARCHAR2(200);
  LV_EMP_FILE VARCHAR2(200);
  LN_TOTAL_SAL NUMBER;
  LN_EMP_COUNT NUMBER;
  LN_CNT NUMBER;
  
CURSOR C1 IS SELECT * FROM MST_ORG_DTLS OD
WHERE OD.ORG_STATUS = 'A' AND OD.ORG_SALARY_DATE = TO_CHAR(SYSDATE+1,'DD');

BEGIN
  SELECT COUNT(*) INTO LN_CNT FROM MST_ORG_DTLS OD
  WHERE OD.ORG_STATUS = 'A' AND OD.ORG_SALARY_DATE = TO_CHAR(SYSDATE+1,'DD');

  IF LN_CNT = 0 THEN
    RETURN;
  END IF;
  LV_ORG_FILE := 'ORG_LIST_FOR_SAL_CRD_' || TO_CHAR(SYSDATE,'DD_MM_YYYY') || '.CSV';

  FO := UTL_FILE.FOPEN('DATA_PUMP_BSM', LV_ORG_FILE, 'W');

  UTL_FILE.PUT_LINE(FO,
    'ORG_NAME,ORG_PHONE,ORG_MAIL_ID,ORG_ADDRESS,ORG_CP_NAME,ORG_CP_MAIL,ORG_SAL_DATE');

  FOR I IN C1 LOOP
     UTL_FILE.PUT_LINE(FO,
      I.ORG_NAME || ',' ||
      I.ORG_PHONE_NUM || ',' ||
      I.ORG_MAIL_ID || ',' ||
      '"' || I.ORG_ADDRESS || '"' || ',' ||
      I.ORG_CP_NAME || ',' ||
      I.ORG_CP_MAIL_ID || ',' ||
      
      TO_CHAR(
        TO_DATE(
          I.ORG_SALARY_DATE || '-' || TO_CHAR(SYSDATE,'MM-YYYY'),'DD-MM-YYYY'),
        'DD-MM-YYYY'));

  END LOOP;

  UTL_FILE.FCLOSE(FO);

  FOR I IN C1 LOOP

    LV_EMP_FILE := I.ORG_NAME || '_EMP_SALARY_DTLS_' || TO_CHAR(SYSDATE,'DD_MM_YYYY') || '.CSV';

    FO := UTL_FILE.FOPEN('DATA_PUMP_BSM', LV_EMP_FILE, 'W');

    UTL_FILE.PUT_LINE(FO, 'EMP_ID,EMP_NAME,ORG_NAME,SALARY');

    
    LN_TOTAL_SAL:=0;
    LN_EMP_COUNT:=0;
    
    FOR J IN (SELECT EMP_ID, EMP_NAME, SALARY
      FROM ORG_EMP_DTLS OE WHERE OE.ORG_ID=I.ORG_ID AND OE.EMP_STATUS='A') LOOP

      UTL_FILE.PUT_LINE(FO,
        J.EMP_ID || ',' ||
        J.EMP_NAME || ',' ||
        I.ORG_NAME || ',' ||
        J.SALARY
      );

      LN_TOTAL_SAL := LN_TOTAL_SAL + J.SALARY;
      LN_EMP_COUNT := LN_EMP_COUNT + 1;

    END LOOP;

    UTL_FILE.PUT_LINE(FO,
      'EMP_COUNT'||',' || LN_EMP_COUNT || ','||'TOTAL_SAL'||','|| LN_TOTAL_SAL
    );

    UTL_FILE.FCLOSE(FO);

  END LOOP;

END SP_TOMORROW_SAL_FILES;



