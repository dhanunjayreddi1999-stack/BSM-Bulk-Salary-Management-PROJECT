CREATE OR REPLACE PROCEDURE SP_EMP_CALCULATION AS
  CURSOR C1 IS
    SELECT OD.ORG_ID, OD.ORG_NAME
      FROM MST_ORG_DTLS OD
     WHERE OD.ORG_STATUS = 'A';
  X           BOOLEAN;
  Y           INTEGER;
  Z           BINARY_INTEGER;
  LV_EMP_FILE VARCHAR2(500);
  LV_QUERY    VARCHAR2(1000);
  LN_EXT_SAL  NUMBER;
  LN_EXT_CNT  NUMBER;
  LV_STATUS   VARCHAR2(10);
  LN_DB_SAL   NUMBER;
  LN_DB_CNT   NUMBER;
BEGIN
  DELETE FROM MONTHLY_ORG_SAL_DTLS;

  FOR I IN C1 LOOP
    FOR J IN (SELECT OE.ORG_ID, SUM(OE.SALARY) SUM_SAL, COUNT(*) CNT
                FROM ORG_EMP_DTLS OE
               WHERE OE.EMP_STATUS = 'A'
                 AND OE.ORG_ID = I.ORG_ID
               GROUP BY OE.ORG_ID) LOOP
    
      INSERT INTO MONTHLY_ORG_SAL_DTLS
      VALUES
        (I.ORG_ID,
         TO_CHAR(SYSDATE, 'MM'),
         TO_CHAR(SYSDATE, 'YYYY'),
         J.SUM_SAL,
         J.CNT);
    END LOOP;
  END LOOP;

  DELETE FROM MONTHLY_ORG_SAL_STATUS;

  FOR I IN C1 LOOP
  
    BEGIN
      SELECT MO.TOTAL_SAL, MO.EMPLOYEES_COUNT
        INTO LN_DB_SAL, LN_DB_CNT
        FROM MONTHLY_ORG_SAL_DTLS MO
       WHERE ORG_ID = I.ORG_ID;
    
    
      LV_EMP_FILE := I.ORG_NAME || '_' || TO_CHAR(SYSDATE, 'MM_YYYY') ||
                     '_EMPLOYEES.CSV';
    
      UTL_FILE.FGETATTR('DATA_PUMP_BSM', LV_EMP_FILE, X, Y, Z);
    
      IF NOT X THEN
        RAISE_APPLICATION_ERROR(-20001, I.ORG_NAME || ' FILE NOT FOUND');
      END IF;
    
      LV_QUERY := 'ALTER TABLE EXT_MONTHLY_ORG_EMP_DTLS LOCATION (''' ||
                  LV_EMP_FILE || ''')';
      EXECUTE IMMEDIATE LV_QUERY;
    
      SELECT SUM(SALARY), COUNT(*)
        INTO LN_EXT_SAL, LN_EXT_CNT
        FROM EXT_MONTHLY_ORG_EMP_DTLS;
    
      INSERT INTO MONTHLY_ORG_SAL_STATUS
        (ORG_ID, MONTH, YEAR, AMOUNT_DIFF, EMPLOYEES_DIFF, MOTHLY_STATUS)
      VALUES
        (I.ORG_ID,
         TO_NUMBER(TO_CHAR(SYSDATE, 'MM')),
         TO_NUMBER(TO_CHAR(SYSDATE, 'YYYY')),
         
         ABS(LN_DB_SAL - LN_EXT_SAL),
         ABS(LN_DB_CNT - LN_EXT_CNT),
         
         CASE WHEN LN_DB_SAL = LN_EXT_SAL AND LN_DB_CNT = LN_EXT_CNT THEN 'S' ELSE 'F' END);
    
      SELECT MO.MOTHLY_STATUS
        INTO LV_STATUS
        FROM MONTHLY_ORG_SAL_STATUS MO;
      IF LV_STATUS = 'S' THEN
        UPDATE MONTHLY_SAL_CRD_DTLS MS
           SET MS.SAL_GEN_DATE = TRUNC(SYSDATE), MS.SAL_CRD_STATUS = 'C'
         WHERE MS.SAL_GEN_DATE IS NULL
           AND MS.ORG_NAME = I.ORG_NAME;
      END IF;
    EXCEPTION
      WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE(I.ORG_NAME || ' - ' || SQLERRM);
      
    END;
  
  END LOOP;

END SP_EMP_CALCULATION;

