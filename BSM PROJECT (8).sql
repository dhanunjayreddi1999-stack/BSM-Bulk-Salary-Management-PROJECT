  (ORG_ID, ORG_NAME, ORG_PHONE_NUM, ORG_MAIL_ID, ORG_ADDRESS,
   ORG_CP_NAME, ORG_CP_MAIL_ID, ORG_DEAL_DATE,
   ORG_NO_OF_EMPLOYEES, ORG_SALARY_DATE, ORG_STATUS, MODIFIED_DATE)

  SELECT ORG_ID, ORG_NAME, ORG_PHONE_NUM, ORG_MAIL_ID, ORG_ADDRESS,
         ORG_CP_NAME, ORG_CP_MAIL_ID, ORG_DEAL_DATE,
         ORG_NO_OF_EMPLOYEES, ORG_SALARY_DATE, ORG_STATUS, SYSDATE
  FROM MST_ORG_DTLS
  WHERE ORG_STATUS = 'X';

  INSERT INTO ORG_EMP_DTLS@BSM_BKP_LINK
  (EMP_ID, EMP_NAME, SALARY, ORG_ID, PHONE_NUM,
   MAIL_ID, DATE_OF_ACTIVE, EMP_STATUS, MODIFIED_DATE)

