CREATE OR REPLACE PROCEDURE SP_EMP_COMPARE AS
  X           BOOLEAN;
  Y           INTEGER;
  Z           BINARY_INTEGER;
  LV_EMP_FILE VARCHAR2(100);
  LV_QUERY    VARCHAR2(1000);
  LV_ORG_FILE VARCHAR2(100);
  CURSOR C1 IS
    SELECT MS.ORG_ID, MD.ORG_NAME
      FROM MONTHLY_ORG_SAL_STATUS MS
      JOIN MST_ORG_DTLS MD
        ON MS.ORG_ID = MD.ORG_ID
     WHERE MS.MOTHLY_STATUS = 'F';
  FO UTL_FILE.file_type;
BEGIN
  FOR I IN C1 LOOP
    LV_EMP_FILE := I.ORG_NAME || '_' || TO_CHAR(SYSDATE, 'MM_YYYY') ||
                   '_EMPLOYEES.CSV';
  
