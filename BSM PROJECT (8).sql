CREATE OR REPLACE TRIGGER TRG_ORG_EMP_DTLS
AFTER INSERT OR UPDATE OR DELETE ON ORG_EMP_DTLS
FOR EACH ROW
DECLARE
  LV_REMARKS VARCHAR2(50);
BEGIN

  IF INSERTING THEN
    LV_REMARKS := 'NEW EMPLOYEE ADDED';

    INSERT INTO AUDIT_ORG_EMP_DTLS
    VALUES (:NEW.ORG_ID, :NEW.EMP_NAME, NULL, :NEW.SALARY, :NEW.EMP_STATUS, LV_REMARKS);

  ELSIF UPDATING THEN
    LV_REMARKS := 'SALARY UPDATED';

    INSERT INTO AUDIT_ORG_EMP_DTLS
    VALUES (:NEW.ORG_ID, :NEW.EMP_NAME, :OLD.SALARY, :NEW.SALARY, :NEW.EMP_STATUS, LV_REMARKS);

  ELSIF DELETING THEN
    LV_REMARKS := 'EMPLOYEE DELETED';

    INSERT INTO AUDIT_ORG_EMP_DTLS
    VALUES (:OLD.ORG_ID, :OLD.EMP_NAME, :OLD.SALARY, NULL, :OLD.EMP_STATUS, LV_REMARKS);

  END IF;

END;

